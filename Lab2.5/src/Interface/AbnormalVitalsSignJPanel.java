    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        int selectedrow = tblVitalSignsAbnormal.getSelectedRow();
        if (selectedrow>=0)
        {
            VitalSigns vs =(VitalSigns)tblVitalSignsAbnormal.getValueAt(selectedrow, 0);
            bloodPressureTextField1.setText(String.valueOf(vs.getBloodPressure()));
            tempTextField1.setText(String.valueOf(vs.getTemperature()));
            pulseTextField1.setText(String.valueOf(vs.getPulse()));
            dateTextField1.setText(vs.getDate());
        }
        else
        {
            JOptionPane.showMessageDialog(null, "please Select any Row");
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed

        int selectedrow = tblVitalSignsAbnormal.getSelectedRow();
        if (selectedrow>=0)
        {
            VitalSigns vs =(VitalSigns)tblVitalSignsAbnormal.getValueAt(selectedrow, 0);
            vsh.deletevitals(vs);
            JOptionPane.showMessageDialog(null, "Vitalsign has been Deleted");
            populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "please Select any Row");
        }
    }//GEN-LAST:event_btnDelActionPerformed


