    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblVitalSignsAbnormal.getModel();
        dtm.setRowCount(0);
        
        for(VitalSigns vs : vsh.getAbnormalList(maxValue, minValue)){
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
    }

